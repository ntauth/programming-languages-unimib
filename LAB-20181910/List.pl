ishead(X, [X | _]).
nth(X, 0, [X | _]).
nth(X, N, [_ | T]) :- N1 is N-1, nth(X, N1, T).

contains([X | _], X).
contains([_ | T], Y) :- contains(T, Y).

append(L, [], L).
append([H | T1], [H | T2], L) :- append(T1, T2, L).

sorted([]).
sorted([_]).
sorted([X, Y | T]) :- X =< Y, sorted([Y | T]).

last([X], X).
last([_ | T], Y) :- last(T, Y).




























































































































































































































































































